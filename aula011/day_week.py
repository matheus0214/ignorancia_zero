""" Day of week """

DAY = int(input("Digite o dia da semana: "))

if DAY == 1:
    print("Domingo")
elif DAY == 2:
    print("Segunda")
elif DAY == 3:
    print("Terça")
elif DAY == 4:
    print("Quarta")
elif DAY == 5:
    print("Quinta")
elif DAY == 6:
    print("Sexta")
elif DAY == 7:
    print("Sábado")
else:
    print("Valor inválido")
